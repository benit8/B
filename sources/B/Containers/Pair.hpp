namespace B
{

////////////////////////////////////////////////////////////////////////////////

template <typename Key, typename Value>
struct Pair
{
	Key key;
	Value value;

	Pair() = default;
	Pair(const Pair& other) = default;
	Pair(Pair&& other) = default;

	Pair(const Key& k, const Value& v)
	: key(k)
	, value(v)
	{}
};

////////////////////////////////////////////////////////////////////////////////

}